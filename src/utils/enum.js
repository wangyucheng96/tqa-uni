// 根据枚举类型获取str/num形式的枚举数组
export const enumMapConvert = (type, val, lang) => {
  if (val) {
    let subArr = map[type + '-' + val]
    if (subArr) {
      if (lang == 'en') {
        let subMap = map[type]
        if (subMap) {
          subArr = subArr.map(list => {
            let arr = [].concat(list)
            arr[1] = subMap[val+'-'+lang]
            return arr
          })
        }
      }
      return subArr
    }
  }
  else {
    let subMap = map[type]
    if (subMap)
      return subMap
  }
  return null
}

// 根据枚举类型把name/num转换成display
export const enumConvert = (type, val, lang) => {
  let nval = '' + val
  if (nval) {
    let subMap = map[type]
    if (lang == 'en')
      nval = nval + '-' + lang
    if (subMap && subMap[nval] !== undefined && subMap[nval] !== null)
      return subMap[nval]
  }
  return val
}

// 根据枚举类型把多个name/num转换成display，多个之间用逗号分隔
export const enumsConvert = (type, vals, lang) => {
  let nval = '' + vals
  if (nval) {
    let subMap = map[type]
    if (subMap) {
      let array = nval.split(',')
      let result = ''
      array.forEach(item => {
        if (result.length > 0)
          result += ','
        let item2 = item
        if (lang == 'en')
          item2 = item2 + '-' + lang
        if (subMap[item2] !== undefined && subMap[item2] !== null)
          result += subMap[item2]
      })
      return result
    }
  }
  return vals
}

// 根据枚举类型把name/num转换成颜色数字
